def run_sandbox_mode(task_text: str, adapter: Coder3ToolAdapter) -> Dict:
    blocks = adapter.extract_python_blocks(task_text)
    code = blocks[0] if blocks else task_text
    syntax = adapter.check_python(code)
    if not syntax['ok']:
        return {'ok': False, 'summary': 'Sandbox остановлен: код не проходит синтаксис.', 'details': {'syntax': syntax}}
    result = adapter.run_sandbox(code)
    stdout = result.get('stdout', '')
    stderr = result.get('stderr', '')
    adapter.create_file('sandbox/last_run.py', code)
    adapter.create_file('sandbox/stdout.txt', stdout)
    adapter.create_file('sandbox/stderr.txt', stderr)
    # Unified output.txt — stdout + stderr combined (для единого архива ТЗ)
    output_parts = []
    if stdout.strip():
        output_parts.append('=== stdout ===\n' + stdout.strip())
    if stderr.strip():
        output_parts.append('=== stderr ===\n' + stderr.strip())
    if not output_parts:
        output_parts.append('(no output)')
    adapter.create_file('sandbox/output.txt', '\n\n'.join(output_parts))
    package = adapter.package_workspace('sandbox_bundle.zip')
    return {'ok': result.get('ok', False), 'summary': 'Sandbox выполнен, наблюдения собраны.', 'details': {'runtime': result, 'package': package}}
